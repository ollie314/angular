/** Special keycodes used by jsaction for the generic click action. */
export enum KeyCode {
  /**
   * If on a Macintosh with an extended keyboard, the Enter key located in the
   * numeric pad has a different ASCII code.
   */
  MAC_ENTER = 3,

  /** The Enter key. */
  ENTER = 13,

  /** The Space key. */
  SPACE = 32,
}
